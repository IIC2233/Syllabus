        expected = [
                        (3,3), (0,5), (6,1), (1,0), (2,4),
                        (4,2), (0,2), (9,3), (3,1), (1,5),
                        (2,0), (10,4), (0,0), (7,2), (4,4),
                        (3,5), (1,3), (2,2), (4,0), (0,4)
                    ]

        result = tablero.optimizar_solucion(change_list)

        self.assertIsNotNone(result)
        self.assertCountEqual(result, expected)

